# Question:
# Please introduce GraphQL

# Answer:
# GraphQL is a query language for APIs 
# and a runtime for fulfilling those queries with your existing data.
# GraphQL will allow user use a JSON like query (JSON without data) to
# precisely request the data. JSON like query will be more flexible 
# when you need some change in API. 

