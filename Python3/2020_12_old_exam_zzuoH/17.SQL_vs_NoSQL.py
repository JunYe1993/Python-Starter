# Question:
# compare SQL and NoSQL

# Answer
# SQL databases are primarily called as Relational Databases (RDBMS); 
# whereas NoSQL database are primarily called as non-relational or distributed database.

# SQL databases defines and manipulates data based structured query language (SQL)
# SQL requires you to use predefined schemas to determine the structure of your data 
# before you work with it.

# A NoSQL database has dynamic schema for unstructured data.
# This flexibility means that documents can be created without having defined structure first.